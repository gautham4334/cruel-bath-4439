let livescores=document.querySelector("#navbar>p");
livescores.addEventListener("mouseover",function(){
    livescores.style.boxShadow="rgba(0, 0, 0, 0.35) 0px 5px 15px";
    let a=document.querySelector(".modals>div");
    a.style.display="block";
    let b=document.querySelector(".modals");
    b.style.display="block";
})
livescores.addEventListener("mouseout",function(){
    livescores.style.boxShadow="none";
    let a=document.querySelector(".modals>div");
    a.style.display="none";
    let b=document.querySelector(".modals");
    b.style.display="none";

})
